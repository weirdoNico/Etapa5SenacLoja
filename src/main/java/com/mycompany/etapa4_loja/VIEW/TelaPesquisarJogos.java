    private void executarBusca() {
        String pesquisa = txtPesquisa.getText().trim();
        String filtro = cbFiltro.getSelectedItem().toString();

        try {
            List<Jogo> filtrados = jogoJPA.buscar(pesquisa, filtro);
            if (filtrados.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Nenhum jogo encontrado!");
            }
            preencherTabela(filtrados);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }

    private List<Jogo> listaAtual;

    public void preencherTabela(List<Jogo> filtrados) {
        DefaultTableModel model = (DefaultTableModel) tblJogos.getModel();
        model.setRowCount(0);
        this.listaAtual = filtrados;
        for (Jogo j : filtrados) {

            String categorias = j.getCategorias().toString();

            model.addRow(new Object[]{
                j.getNome(),
                j.getDesenvolvedora(),
                j.getClassificacaoIndicativa(),
                categorias
            });

        }
    }

