    private void btnAddCarrinhoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCarrinhoActionPerformed
        int linhaSelecionada = tblJogos.getSelectedRow();
        Jogo selecionado = listaAtual.get(linhaSelecionada);
        
        

        if (selecionado instanceof JogoLocavel && !CarrinhoCompra.getItens().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Seu carrinho já possui jogos para COMPRA.\nNão é permitido misturar com jogos para LOCAÇÃO.");
            return;
        }

        if (selecionado instanceof JogoCompravel && !CarrinhoLocacao.getItens().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Seu carrinho já possui jogos para LOCAÇÃO.\nNão é permitido misturar com jogos para COMPRA.");
            return;
        }

        if (selecionado instanceof JogoCompravel) {
            CarrinhoCompra.adicionar((JogoCompravel) selecionado);
            JOptionPane.showMessageDialog(null, "Jogo adicionado ao carrinho de COMPRA!");
        } else if (selecionado instanceof JogoLocavel) {
            JogoLocavel locavel = (JogoLocavel) selecionado;

            CarrinhoLocacao.adicionar(locavel);
            JOptionPane.showMessageDialog(null, "Jogo adicionado ao carrinho de LOCAÇÃO!");
        }

        
    }//GEN-LAST:event_btnAddCarrinhoActionPerformed

    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        List<JogoCompravel> itensCompra = CarrinhoCompra.getItens();
        List<JogoLocavel> itensLocacao = CarrinhoLocacao.getItens();

        if (itensCompra.isEmpty() && itensLocacao.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Carrinho vazio!");
            return;
        }

        if (!itensCompra.isEmpty()) {
            new TelaCarrinhoCompra().setVisible(true);
            this.dispose();
        } else if (!itensLocacao.isEmpty()) {
            new TelaCarrinhoLocacao().setVisible(true);
            this.dispose();
        }


    }//GEN-LAST:event_btnContinuarActionPerformed

