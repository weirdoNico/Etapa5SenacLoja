    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        if (clienteSelecionado == null) {
            JOptionPane.showMessageDialog(null, "Selecione e vincule um cliente primeiro!");
            return;
        }

        List<JogoCompravel> itensNoCarrinho = CarrinhoCompra.getItens();

        if (itensNoCarrinho.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O carrinho está vazio!");
            return;
        }

        LocalDate hoje = LocalDate.now();
        String forma = cbFormaDePagar.getSelectedItem().toString();
        List<JogoCompravel> compraveis = new ArrayList<>();
        
        try{
        for (JogoCompravel j : itensNoCarrinho) {
            jogoCompravelJPA.registrarVenda(j);
            
            j.setQuantidadeEstoque(j.getQuantidadeEstoque() - 1);

            compraveis.add(j);

        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Houve um erro ao tentar comprar o jogo.");
        }

        Compra novaCompra = new Compra(hoje, forma, clienteSelecionado, calcularTotalCompra(), compraveis);
        compraJPA.criar(novaCompra);

        JOptionPane.showMessageDialog(null, "Compra de " + compraveis.size() + " jogo(s) finalizada!");
        CarrinhoCompra.limpar();
        itensNoCarrinho.clear();

        clienteSelecionado = null;
        TelaPesquisarJogos tela = new TelaPesquisarJogos();
        tela.setVisible(true);
        this.dispose();

    }//GEN-LAST:event_btnFinalizarActionPerformed

