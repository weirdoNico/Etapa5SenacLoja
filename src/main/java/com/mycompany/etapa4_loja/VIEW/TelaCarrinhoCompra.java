    public float calcularTotalCompra() {
        float total = 0;

        for (JogoCompravel j : CarrinhoCompra.getItens()) {
            total += j.getPreco();
        }
        if (CarrinhoCompra.getItens().isEmpty()) {
            total = 0;
        }
        return total;

    }

    public void atualizarValorTotal() {
        float total = calcularTotalCompra();

        String texto = String.format("R$ %.2f", total);

        lblValor.setText(texto);
    }

    public void preencherCarrinho(List<JogoCompravel> jogos) {

        DefaultTableModel model = (DefaultTableModel) tblCarrinho.getModel();
        model.setRowCount(0);
        this.listaAtualJogo = jogos;

        for (JogoCompravel j : jogos) {

            model.addRow(new Object[]{
                j.getNome(),
                j.getPreco()
            });
        }
    }

