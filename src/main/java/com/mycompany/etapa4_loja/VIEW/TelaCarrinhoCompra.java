    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String pesquisa = txtConta.getText().trim().toLowerCase();
        List<Cliente> todos = clienteJPA.listarTodos();
        List<Cliente> filtrado = new ArrayList<>();

        if (todos.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum cliente cadastrado no sistema!");
            return;
        }

        if (pesquisa.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite o nome do cliente para buscar");
            return;
        }

        for (Cliente c : todos) {
            if (c.getNome().toLowerCase().contains(pesquisa)) {
                filtrado.add(c);
            }
        }
        if (filtrado.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nenhum cliente encontrado!");
        }
        preencherTabela(filtrado);


    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnVincularClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVincularClienteActionPerformed
        int linhaSelecionada = tblContas.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
            return;
        }

        clienteSelecionado = listaAtual.get(linhaSelecionada);

        lblCliente.setText(clienteSelecionado.getNome());


    }//GEN-LAST:event_btnVincularClienteActionPerformed

    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        int linhaSelecionada = tblCarrinho.getSelectedRow();
        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
            return;
        }

        JogoCompravel selecionado = listaAtualJogo.get(linhaSelecionada);

        CarrinhoCompra.remover(selecionado);

        preencherCarrinho(CarrinhoCompra.getItens());
    }//GEN-LAST:event_btnRetirarActionPerformed

